def display_pred(true, preds, name):
    plt.figure()
    if preds is not None:
        plt.plot(preds, label='Prediction', c='coral')
    plt.plot(true, label='GroundTruth', c='#1f77b4')
    plt.legend()
    plt.savefig(name, bbox_inches='tight')



'''
    MAE
'''
def MAE(pred, true):
    return np.mean(np.abs(pred - true))


'''
    MSE
'''
def MSE(pred, true):
    return np.mean((pred - true) ** 2)